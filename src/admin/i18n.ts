export function fieldLabel(key: string, lang: Lang): string {
  const f = FIELD[key];
  if (f) return f[lang === "de" ? 0 : 1];
  // humanize unknown keys
  return key.replace(/([A-Z])/g, " $1").replace(/^./, (c) => c.toUpperCase());
}
